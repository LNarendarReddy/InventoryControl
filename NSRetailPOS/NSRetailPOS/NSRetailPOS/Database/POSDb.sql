CREATE PROCEDURE [dbo].[POS_USP_R_BILL] 
	@BillID INT, @DaySequencID INT
AS
BEGIN

	DECLARE @LastBillID INT
	SELECT @LastBillID = LASTBILLID FROM POS_DAYSEQUENCE WHERE DAYSEQUENCEID = @DaySequencID

	DECLARE @LastBilledAmount DECIMAL(11, 2)
	DECLARE @LastBilledQuantity INT

	SELECT @LastBilledAmount = SUM(BD.BILLEDAMOUNT), @LastBilledQuantity = SUM(BD.QUANTITY) FROM POS_BILLDETAIL BD WHERE BD.BILLID = @LastBillID AND BD.DELETEDBY IS NULL

	SELECT 
		B.BILLID, B.BILLNUMBER, B.BILLSTATUS, @LastBilledAmount AS LASTBILLEDAMOUNT, @LastBilledQuantity AS LASTBILLEDQUANTITY
	FROM 
		POS_BILL B
		LEFT JOIN POS_BILL LASTBILL ON LASTBILL.BILLID = @LastBillID	
	WHERE B.BILLID = @BillID

	SELECT
		BD.BILLDETAILID, B.BILLID, IP.ITEMPRICEID, BD.SNO, I.ITEMNAME, IC.ITEMCODE, IP.MRP
		, IP.SALEPRICE, GST.GSTCODE, BD.QUANTITY, BD.WEIGHTINKGS, BD.BILLEDAMOUNT, 
		BD.CGST, BD.SGST, BD.IGST, BD.CESS, BD.GSTVALUE, BD.GSTID
	FROM
		POS_BILLDETAIL BD
		INNER JOIN POS_BILL B ON B.BILLID = BD.BILLID
		INNER JOIN POS_ITEMPRICE IP ON IP.ITEMPRICEID = BD.ITEMPRICEID
		INNER JOIN POS_ITEMCODE IC ON IC.ITEMCODEID = IP.ITEMCODEID
		INNER JOIN POS_ITEM I ON I.ITEMID = IC.ITEMID
		INNER JOIN POS_GSTDETAIL GST ON GST.GSTID = IP.GSTID
	WHERE B.BILLID = @BillID AND BD.DELETEDBY IS NULL
	ORDER BY BD.SNO
END
