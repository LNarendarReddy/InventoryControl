CREATE PROCEDURE [dbo].[POS_USP_R_LOAD]
@UserID INT, @BranchCounterID INT
AS
BEGIN
	DECLARE @DaySequenceID INT

	SELECT TOP 1 @DaySequenceID = DAYSEQUENCEID FROM POS_DAYSEQUENCE ORDER BY OPENDATE DESC 

	IF ISNULL(@DaySequenceID, 0) = 0 OR (SELECT OPENDATE FROM POS_DAYSEQUENCE WHERE DAYSEQUENCEID = @DaySequenceID) <> CAST(GETDATE() AS DATE)
	BEGIN
		INSERT INTO POS_DAYSEQUENCE(OPENDATE, BRANCHCOUNTERID)
		SELECT CAST(GETDATE() AS DATE), @BranchCounterID

		SET @DaySequenceID = SCOPE_IDENTITY()
	END
	ELSE IF (SELECT ISCLOSED FROM POS_DAYSEQUENCE WHERE DAYSEQUENCEID = @DaySequenceID) = 1
	BEGIN 
		SELECT 'Billing closed'
		RETURN
	END

	DECLARE @LastBillNum BIGINT
	SELECT @LastBillNum = ISNULL(CAST(LASTUSEDBILLNUM AS BIGINT), 0) FROM POS_DAYSEQUENCE WHERE DAYSEQUENCEID = @DaySequenceID

	IF @LastBillNum = 0
	BEGIN
		SELECT @LastBillNum = FORMAT(GETDATE(), 'yyyyMMdd') + '000000'

		UPDATE POS_DAYSEQUENCE
		SET LASTUSEDBILLNUM = @LastBillNum
		WHERE DAYSEQUENCEID = @DaySequenceID
	END
	
	SELECT @DaySequenceID AS DAYSEQUENCEID

	-- find open bill for that particular user
	DECLARE @OpenBillID INT
	SELECT @OpenBillID = OPENBILLID
	FROM 
		POS_DAYSEQUENCE DS 
		INNER JOIN POS_BILL B ON B.CREATEDBY = @UserID
	WHERE DAYSEQUENCEID = @DaySequenceID

	IF ISNULL(@OpenBillID, 0) = 0
	BEGIN
		EXEC POS_USP_R_GETNEXTBILL @UserID, @DaySequenceID
	END
	ELSE
	BEGIN
		EXEC POS_USP_R_BILL @OpenBillID, @DaySequenceID
	END
END
GO
